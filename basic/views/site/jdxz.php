    <h1>人体伤情法医鉴定须知</h1>
    <hr>
    <p>1.办案民警与伤者一起到法医鉴定机构，并携带数字化物证鉴定系统预约成功后打印的鉴定委托书（需加盖单位公章）。

    <p>
        2.提供所有病历资料（加盖就诊医院、办案单位公章），包括门、急诊病历、住院病历、手术记录、出院小结、其他辅助检查报告及B超、X片、CT片、MRI片等原始影像图片。必要时可能需要提供化验报告单、护理记录、病程记录及影像电子数据库资料（常用光盘、U盘拷贝）等。

    <p>3.被鉴定人持有有效身份证件。首选身份证，其次可以是户口簿、驾驶证、军官证、护照等。

    <p>4.伤者本人来法医鉴定机构接受检验，鉴定人根据具体伤情可以进行必要的医学检查、复查和其他相应措施。

    <p>5.凡涉及损伤影响面容或者功能的，如面部损伤，神经、肌腱、关节损伤，肢体骨折，视力、听力障碍的，一般自受伤之日起90日后进行鉴定。

    <p>6.伤情鉴定材料齐备，鉴定人受理后，与委托单位签订鉴定事项确认书，收取鉴定资料复印复制件或原件，一般3-7日发放鉴定文书，特殊情况双方约定出具鉴定文书时间。收取鉴定资料原件的，此时一并退回。

    <p>7.疑难、复杂、有争议鉴定，鉴定人有权进一步了解案情，调阅卷宗。必要时，可以采取现场勘验、会诊鉴定、模拟实验等形式。

