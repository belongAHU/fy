            <p>a)手功能丧失累计达一手功能36%。
            <p>b)一手拇指挛缩畸形不能对指和握物。
            <p>c)一手除拇指外，其余任何三指挛缩畸形，不能对指和握物。
            <p>d)一手拇指离断或者缺失超过指间关节。
            <p>e)一手食指和中指全部离断或者缺失。
            <p>f)一手除拇指外的任何三指离断或者缺失均超过近侧指间关节。
            <h4>5.10.3 轻伤一级</h4>
            <p>a)手功能丧失累计达一手功能16%。
            <p>b)一手拇指离断或者缺失未超过指间关节。
            <p>c)一手除拇指外的示指和中指离断或者缺失均超过远侧指间关节。
            <p>d)一手除拇指外的环指和小指离断或者缺失均超过近侧指间关节。
            <h4>5.10.4 轻伤二级</h4>
            <p>a)手功能丧失累计达一手功能4%。
            <p>b)除拇指外的一个指节离断或者缺失。
            <p>c)两节指骨线性骨折或者一节指骨粉碎性骨折（不含第2至5指末节）。
            <p>d)舟骨骨折、月骨脱位或者掌骨完全性骨折。
            <h4>5.10.5 轻微伤</h4>
            <p>a)手擦伤面积10.0cm2以上或者挫伤面积6.0cm2以上。
            <p>b)手一处创口或者瘢痕长度1.0cm以上；两处以上创口或者瘢痕长度累计1.5cm以上；刺伤深达肌层。
            <p>c)手关节或者肌腱损伤。
            <p>d)腕骨、掌骨或者指骨骨折。
            <p>e)外伤致指甲脱落，甲床暴露；甲床出血。

            <h3 id="section-5-11">5.11　体表</h3>

            <h4>5.11.1 重伤二级</h4>
            <p>a)挫伤面积累计达体表面积30%。
            <p>b)创口或者瘢痕长度累计200.0cm以上。
            <h4>5.11.2 轻伤一级</h4>
            <p>            a)挫伤面积累计达体表面积10%。
            <p>b)创口或者瘢痕长度累计40.0cm以上。
            <p>c)撕脱伤面积100.0cm2以上。
            <p>d)皮肤缺损30.0cm2以上。
            <h4>5.11.3 轻伤二级</h4>
            <p>a)挫伤面积达体表面积6%。
            <p>b)单个创口或者瘢痕长度10.0cm以上；多个创口或者瘢痕长度累计15.0cm以上。
            <p>c)撕脱伤面积50.0cm2以上。
            <p>d)皮肤缺损6.0cm2以上。
            <h4>5.11.4 轻微伤</h4>
            <p>            a)擦伤面积20.0cm2以上或者挫伤面积15.0cm2以上。
            <p>b)一处创口或者瘢痕长度1.0cm以上；两处以上创口或者瘢痕长度累计1.5cm以上；刺创深达肌层。
            <p>c)咬伤致皮肤破损。


            <h3 id="section-5-12">5.12　其他</h3>

            <h4>5.12.1 重伤一级</h4>
            <p>            a)深IIo以上烧烫伤面积达体表面积70%或者IIIo面积达30%。
            <h4>5.12.2 重伤二级</h4>
            <p>a)IIo以上烧烫伤面积达体表面积30%或者IIIo面积达10%；面积低于上述程度但合并吸入有毒气体中毒或者严重呼吸道烧烫伤。
            <p>b)枪弹创，创道长度累计180.0cm。
            <p>c)各种损伤引起脑水肿（脑肿胀），脑疝形成。
            <p>d)各种损伤引起休克（中度）。
            <p>e)挤压综合征（II级）。
            <p>f)损伤引起脂肪栓塞综合征（完全型）。
            <p>g)各种损伤致急性呼吸窘迫综合征(重度)。
            <p>h)电击伤（IIo）。
            <p>i)溺水（中度）。
            <p>j)脑内异物存留；心脏异物存留。
            <p>k)器质性阴茎勃起障碍（重度）。
            <h4>5.12.3 轻伤一级</h4>
            <p>            a)IIo以上烧烫伤面积达体表面积20%或者IIIo面积达5%。
            <p>b)损伤引起脂肪栓塞综合征（不完全型）。
            <p>c)器质性阴茎勃起障碍（中度）。
            <h4>5.12.4 轻伤二级</h4>
            <p>a)IIo以上烧烫伤面积达体表面积5%或者IIIo面积达0.5%。
            <p>b)呼吸道烧伤。
            <p>c)挤压综合征（I级）。
            <p>d)电击伤（Ⅰo）。
            <p>e)溺水（轻度）。
            <p>f)各种损伤引起休克（轻度）。
            <p>g)呼吸功能障碍，出现窒息征象。
            <p>h)面部异物存留；眶内异物存留；鼻窦异物存留。
            <p>i)胸腔内异物存留；腹腔内异物存留；盆腔内异物存留。
            <p>j)深部组织内异物存留。
            <p>k)骨折内固定物损坏需要手术更换或者修复。
            <p>l)各种置入式假体装置损坏需要手术更换或者修复。
            <p>m)器质性阴茎勃起障碍（轻度）。
            <h4>5.12.5 轻微伤</h4>
            <p>a)身体各部位骨皮质的砍（刺）痕；轻微撕脱性骨折，无功能障碍。
            <p>b)面部Ⅰo烧烫伤面积10.0cm2以上；浅IIo烧烫伤。
            <p>c)颈部Ⅰo烧烫伤面积15.0cm2以上；浅IIo烧烫伤面积2.0cm2以上。
            <p>d)体表Ⅰo烧烫伤面积20.0cm2以上；浅IIo烧烫伤面积4.0cm2以上；深IIo烧烫伤。
