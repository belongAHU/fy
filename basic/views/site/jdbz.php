            <h2 id="section-1">1 范围</h2>
            <p>本标准规定了人体损伤程度鉴定的原则、方法、内容和等级划分。</p>
            <p>本标准适用于《中华人民共和国刑法》及其他法律、法规所涉及的人体损伤程度鉴定。</p>
            <hr>
            <h2 id="section-2">2 规范性引用文件</h2>
            <p>下列文件对于本文件的应用是必不可少的。本标准引用文件的最新版本适用于本标准。</p>
            <p>GB 18667 道路交通事故受伤人员伤残评定</p>
            <p>GB/T 16180 劳动能力鉴定 职工工伤与职业病致残等级</p>
            <p>GB/T 26341-2010 残疾人残疾分类和分级</p>
            <hr>
            <h2 id="section-3">3 术语定义</h2>
            <h3>3.1 重伤</h3>
            <p>使人肢体残废、毁人容貌、丧失听觉、丧失视觉、丧失其他器官功能或者其他对于人身健康有重大伤害的损伤，包括重伤一级和重伤二级。</p>
            <h3>3.2 轻伤</h3>
            <p>使人肢体或者容貌损害，听觉、视觉或者其他器官功能部分障碍或者其他对于人身健康有中度伤害的损伤，包括轻伤一级和轻伤二级。</p>
            <h3>3.3 轻微伤</h3>
            <p>各种致伤因素所致的原发性损伤，造成组织器官结构轻微损害或者轻微功能障碍。</p>
            <hr>
            <h2 id="section-4">4 总则</h2>
            <h3>4.1　鉴定原则</h3>
            <p>4.1.1 遵循实事求是的原则，坚持以致伤因素对人体直接造成的原发性损伤及由损伤引起的并发症或者后遗症为依据，全面分析，综合鉴定。</p>
            <p>4.1.2 对于以原发性损伤及其并发症作为鉴定依据的，鉴定时应以损伤当时伤情为主，损伤的后果为辅，综合鉴定。</p>
            <p>4.1.3 对于以容貌损害或者组织器官功能障碍作为鉴定依据的，鉴定时应以损伤的后果为主，损伤当时伤情为辅，综合鉴定。</p>
            <h3>4.2　鉴定时机</h3>
            <p>4.2.1 以原发性损伤为主要鉴定依据的，伤后即可进行鉴定；以损伤所致的并发症为主要鉴定依据的，在伤情稳定后进行鉴定。</p>
            <p>4.2.2 以容貌损害或者组织器官功能障碍为主要鉴定依据的，在损伤90日后进行鉴定；在特殊情况下可以根据原发性损伤及其并发症出具鉴定意见，但须对有可能出现的后遗症加以说明，必要时应进行复检并予以补充鉴定。[1]</p>
            <p>4.2.3 疑难、复杂的损伤，在临床治疗终结或者伤情稳定后进行鉴定。</p>
            <h3>4.3　伤病关系处理原则</h3>
            <p>4.3.1 损伤为主要作用的，既往伤/病为次要或者轻微作用的，应依据本标准相应条款进行鉴定。</p>
            <p>4.3.2 损伤与既往伤/病共同作用的，即二者作用相当的，应依据本标准相应条款适度降低损伤程度等级，即等级为重伤一级和重伤二级的，可视具体情况鉴定为轻伤一级或者轻伤二级，等级为轻伤一级和轻伤二级的，均鉴定为轻微伤。</p>
            <p>4.3.3 既往伤/病为主要作用的，即损伤为次要或者轻微作用的，不宜进行损伤程度鉴定，只说明因果关系。</p>
