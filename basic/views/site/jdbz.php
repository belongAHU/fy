            <h2 id="section-6">6 附则</h2>
            <p>6.1　伤后因其他原因死亡的个体，其生前损伤比照本标准相关条款综合鉴定。</p>
            <p>6.2　未列入本标准中的物理性、化学性和生物性等致伤因素造成的人体损伤，比照本标准中的相应条款综合鉴定。</p>
            <p>6.3　本标准所称的损伤是指各种致伤因素所引起的人体组织器官结构破坏或者功能障碍。反应性精神病、癔症等，均为内源性疾病，不宜鉴定损伤程度。</p>
            <p>6.4　本标准未作具体规定的损伤，可以遵循损伤程度等级划分原则，比照本标准相近条款进行损伤程度鉴定。</p>
            <p>6.5　盲管创、贯通创，其创道长度可视为皮肤创口长度，并参照皮肤创口长度相应条款鉴定损伤程度。</p>
            <p>6.6　牙折包括冠折、根折和根冠折，冠折须暴露髓腔。</p>
            <p>6.7　骨皮质的砍（刺）痕或者轻微撕脱性骨折（无功能障碍）的，不构成本标准所指的轻伤。</p>
            <p>6.8　本标准所称大血管是指胸主动脉、主动脉弓分支、肺动脉、肺静脉、上腔静脉和下腔静脉，腹主动脉、髂总动脉、髂外动脉、髂外静脉。</p>
            <p>6.9　本标准四肢大关节是指肩、肘、腕、髋、膝、踝等六大关节。</p>
            <p>6.10　本标准四肢重要神经是指臂丛及其分支神经（包括正中神经、尺神经、桡神经和肌皮神经等）和腰骶丛及其分支神经（包括坐骨神经、腓总神经、腓浅神经和胫神经等）。</p>
            <p>6.11　本标准四肢重要血管是指与四肢重要神经伴行的同名动、静脉。</p>
            <p>6.12　本标准幼女或者儿童是指年龄不满14周岁的个体。</p>
            <p>6.13　本标准所称的假体是指植入体内替代组织器官功能的装置，如：颅骨修补材料、人工晶体、义眼座、固定义齿（种植牙）、阴茎假体、人工关节、起搏器、支架等，但可摘式义眼、义齿等除外。
            <p>6.14　移植器官损伤参照相应条款综合鉴定。</p>
            <p>6.15　本标准所称组织器官包括再植或者再造成活的。</p>
            <p>6.16　组织器官缺失是指损伤当时完全离体或者仅有少量皮肤和皮下组织相连，或者因损伤经手术切除的。器官离断（包括牙齿脱落），经再植、再造手术成功的，按损伤当时情形鉴定损伤程度。</p>
            <p>6.17　对于两个部位以上同类损伤可以累加，比照相关部位数值规定高的条款进行评定。</p>
            <p>6.18　本标准所涉及的体表损伤数值，0～6岁按50%计算，7～10岁按60%计算，11～14岁按80%计算。</p>
            <p>6.19　本标准中出现的数字均含本数。</p>
