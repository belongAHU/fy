            <h2 id="section-5">5 程度分级</h2>
            <h3 id="section-5-1">5.1　颅脑、脊髓</h3>
            <h4>5.1.1 重伤一级</h4>
            <p>a)植物生存状态。
            <p>b)四肢瘫（三肢以上肌力3级以下）。
            <p>c)偏瘫、截瘫（肌力2级以下），伴大便、小便失禁。
            <p>d)非肢体瘫的运动障碍（重度）。
            <p>e)重度智能减退或者器质性精神障碍，生活完全不能自理。
            <h4>5.1.2 重伤二级</h4>
            <p>a)头皮缺损面积累计75.0cm2以上。
            <p>b)开放性颅骨骨折伴硬脑膜破裂。
            <p>c)颅骨凹陷性或者粉碎性骨折，出现脑受压症状和体征，须手术治疗。
            <p>d)颅底骨折，伴脑脊液漏持续4周以上。
            <p>e)颅底骨折，伴面神经或者听神经损伤引起相应神经功能障碍。
            <p>f)外伤性蛛网膜下腔出血，伴神经系统症状和体征。
            <p>g)脑挫(裂)伤，伴神经系统症状和体征。
            <p>h)颅内出血，伴脑受压症状和体征。
            <p>i)外伤性脑梗死，伴神经系统症状和体征。
            <p>j)外伤性脑脓肿。
            <p>k)外伤性脑动脉瘤，须手术治疗。
            <p>l)外伤性迟发性癫痫。
            <p>m)外伤性脑积水，须手术治疗。
            <p>n)外伤性颈动脉海绵窦瘘。
            <p>o)外伤性下丘脑综合征。
            <p>p)外伤性尿崩症。
            <p>q)单肢瘫（肌力3级以下）。
            <p>r)脊髓损伤致重度肛门失禁或者重度排尿障碍。
            <h4>5.1.3 轻伤一级</h4>
            <p>a)头皮创口或者瘢痕长度累计20.0cm以上。
            <p>b)头皮撕脱伤面积累计50.0cm2以上；头皮缺损面积累计24.0cm2以上。
            <p>c)颅骨凹陷性或者粉碎性骨折。
            <p>d)颅底骨折伴脑脊液漏。
            <p>e)脑挫（裂）伤；颅内出血；慢性颅内血肿；外伤性硬脑膜下积液。
            <p>f)外伤性脑积水；外伤性颅内动脉瘤；外伤性脑梗死；外伤性颅内低压综合征。
            <p>g)脊髓损伤致排便或者排尿功能障碍（轻度）。
            <p>h)脊髓挫裂伤。
            <h4>5.1.4 轻伤二级</h4>
            <p>a)头皮创口或者瘢痕长度累计8.0cm以上。
            <p>b)头皮撕脱伤面积累计20.0cm2以上；头皮缺损面积累计10.0cm2以上。
            <p>c)帽状腱膜下血肿范围50.0cm2以上。
            <p>d)颅骨骨折。
            <p>e)外伤性蛛网膜下腔出血。
            <p>f)脑神经损伤引起相应神经功能障碍。
            <h4>5.1.5 轻微伤</h4>
            <p>a)头部外伤后伴有神经症状。
            <p>b)头皮擦伤面积5.0cm2以上；头皮挫伤；头皮下血肿。
            <p>c)头皮创口或者瘢痕。

            <h3 id="section-5-2">5.2　面部、耳廓</h3>

            <h4>5.2.1 重伤一级</h4>
            <p>a)容貌毁损（重度）。
            <h4>5.2.2 重伤二级</h4>
            <p>a)面部条状瘢痕（50%以上位于中心区），单条长度10.0cm以上，或者两条以上长度累计15.0cm以上。
            <p>b)面部块状瘢痕（50%以上位于中心区），单块面积6.0cm2以上，或者两块以上面积累计10.0cm2以上。
            <p>c)面部片状细小瘢痕或者显著色素异常，面积累计达面部30%。
            <p>d)一侧眼球萎缩或者缺失。
            <p>e)眼睑缺失相当于一侧上眼睑1/2以上。
            <p>f)一侧眼睑重度外翻或者双侧眼睑中度外翻。
            <p>g)一侧上睑下垂完全覆盖瞳孔。
            <p>h)一侧眼眶骨折致眼球内陷0.5cm以上。
            <p>i)一侧鼻泪管和内眦韧带断裂。
            <p>j)鼻部离断或者缺损30%以上。
            <p>k)耳廓离断、缺损或者挛缩畸形累计相当于一侧耳廓面积50%以上。
            <p>l)口唇离断或者缺损致牙齿外露3枚以上。
            <p>m)舌体离断或者缺损达舌系带。
            <p>n)牙齿脱落或者牙折共7枚以上。
            <p>o)损伤致张口困难Ⅲ度。
            <p>p)面神经损伤致一侧面肌大部分瘫痪，遗留眼睑闭合不全和口角歪斜。
            <p>q)容貌毁损（轻度）。
            <h4>5.2.3 轻伤一级</h4>
            <p>a)面部单个创口或者瘢痕长度6.0cm以上；多个创口或者瘢痕长度累计10.0cm以上。
            <p>b)面部块状瘢痕，单块面积4.0cm2以上；多块面积累计7.0cm2以上。
            <p>c)面部片状细小瘢痕或者明显色素异常，面积累计30.0cm2以上。
            <p>d)眼睑缺失相当于一侧上眼睑1/4以上。
            <p>e)一侧眼睑中度外翻；双侧眼睑轻度外翻。
            <p>f)一侧上眼睑下垂覆盖瞳孔超过1/2。
            <p>g)两处以上不同眶壁骨折；一侧眶壁骨折致眼球内陷0.2cm以上。
            <p>h)双侧泪器损伤伴溢泪。
            <p>i)一侧鼻泪管断裂；一侧内眦韧带断裂。
            <p>            j)耳廓离断、缺损或者挛缩畸形累计相当于一侧耳廓面积30%以上。
            <p>k)鼻部离断或者缺损15%以上。
            <p>l)口唇离断或者缺损致牙齿外露1枚以上。
            <p>m)牙齿脱落或者牙折共4枚以上。
            <p>n)损伤致张口困难Ⅱ度。
            <p>o)腮腺总导管完全断裂。
            <p>p)面神经损伤致一侧面肌部分瘫痪，遗留眼睑闭合不全或者口角歪斜。
            <h4>5.2.4 轻伤二级</h4>
            <p>a)面部单个创口或者瘢痕长度4.5cm以上；多个创口或者瘢痕长度累计6.0cm以上。
            <p>b)面颊穿透创，皮肤创口或者瘢痕长度1.0cm以上。
            <p>c)口唇全层裂创，皮肤创口或者瘢痕长度1.0cm以上。
            <p>d)面部块状瘢痕，单块面积3.0cm2以上或多块面积累计5.0cm2以上。
            <p>e)面部片状细小瘢痕或者色素异常，面积累计8.0cm2以上。
            <p>f)眶壁骨折（单纯眶内壁骨折除外）。
            <p>g)眼睑缺损。
            <p>h)一侧眼睑轻度外翻。
            <p>i)一侧上眼睑下垂覆盖瞳孔。
            <p>j)一侧眼睑闭合不全。
            <p>k)一侧泪器损伤伴溢泪。
            <p>l)耳廓创口或者瘢痕长度累计6.0cm以上。
            <p>m)耳廓离断、缺损或者挛缩畸形累计相当于一侧耳廓面积15%以上。
            <p>n)鼻尖或者一侧鼻翼缺损。
            <p>o)鼻骨粉碎性骨折；双侧鼻骨骨折；鼻骨骨折合并上颌骨额突骨折；鼻骨骨折合并鼻中隔骨折；双侧上颌骨额突骨折。
            <p>p)舌缺损。
            <p>q)牙齿脱落或者牙折2枚以上。
            <p>r)腮腺、颌下腺或者舌下腺实质性损伤。
            <p>s)损伤致张口困难Ⅰ度。
            <p>t)颌骨骨折（牙槽突骨折及一侧上颌骨额突骨折除外）。
            <p>u)颧骨骨折。
            <h4>5.2.5 轻微伤</h4>
            <p>a)面部软组织创。
            <p>b)面部损伤留有瘢痕或者色素改变。
            <p>c)面部皮肤擦伤，面积2.0cm2以上；面部软组织挫伤；面部划伤4.0cm以上。
            <p>d)眶内壁骨折。
            <p>e)眼部挫伤；眼部外伤后影响外观。
            <p>f)耳廓创。
            <p>g)鼻骨骨折；鼻出血。
            <p>h)上颌骨额突骨折。
            <p>i)口腔粘膜破损；舌损伤。
            <p>j)牙齿脱落或者缺损；牙槽突骨折；牙齿松动2枚以上或者Ⅲ度松动1枚以上。

            <h3 id="section-5-3">5.3　听器听力</h3>

            <h4>5.3.1 重伤一级</h4>
            <p>a)双耳听力障碍(≥91dB HL)。
            <h4>5.3.2 重伤二级</h4>
            <p>a)一耳听力障碍(≥91dB HL)。
            <p>b)一耳听力障碍(≥81dB HL)，另一耳听力障碍(≥41dB HL)。
            <p>c)一耳听力障碍(≥81dB HL)，伴同侧前庭平衡功能障碍。
            <p>d)双耳听力障碍(≥61dB HL)。
            <p>e)双侧前庭平衡功能丧失，睁眼行走困难，不能并足站立。
            <h4>5.3.3 轻伤一级</h4>
            <p>a)双耳听力障碍(≥41dB HL)。
            <p>b)双耳外耳道闭锁。
            <h4>5.3.4 轻伤二级</h4>
            <p>a)外伤性鼓膜穿孔6周不能自行愈合。
            <p>b)听骨骨折或者脱位；听骨链固定。
            <p>c)一耳听力障碍(≥41dB HL)。
            <p>d)一侧前庭平衡功能障碍，伴同侧听力减退。
            <p>e)一耳外耳道横截面1/2以上狭窄。
            <h4>5.3.5 轻微伤</h4>
            <p>a)外伤性鼓膜穿孔。
            <p>b)鼓室积血。
            <p>c)外伤后听力减退。

            <h3 id="section-5-4">5.4　视器视力</h3>

            <h4>5.4.1 重伤一级</h4>
            <p>a)一眼眼球萎缩或者缺失，另一眼盲目3级。
            <p>b)一眼视野完全缺损，另一眼视野半径20o以下（视野有效值32%以下）。
            <p>c)双眼盲目4级。
            <h4>5.4.2 重伤二级</h4>
            <p>a)一眼盲目3级。
            <p>b)一眼重度视力损害，另一眼中度视力损害。
